package js.admin.model;

public class Street {
	private String admin_no;
	private int sequence_no;
	private String towncode;
	private String province;
	private String district;
	private String town;
	private String subtown;
	private String mountain;
	private int street;
	private int substreet;
	private String representative;
	public String getAdmin_no() {
		return admin_no;
	}
	public void setAdmin_no(String admin_no) {
		this.admin_no = admin_no;
	}
	public int getSequence_no() {
		return sequence_no;
	}
	public void setSequence_no(int sequence_no) {
		this.sequence_no = sequence_no;
	}
	public String getTowncode() {
		return towncode;
	}
	public void setTowncode(String towncode) {
		this.towncode = towncode;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getTown() {
		return town;
	}
	public void setTown(String town) {
		this.town = town;
	}
	public String getSubtown() {
		return subtown;
	}
	public void setSubtown(String subtown) {
		this.subtown = subtown;
	}
	public String getMountain() {
		return mountain;
	}
	public void setMountain(String mountain) {
		this.mountain = mountain;
	}
	public int getStreet() {
		return street;
	}
	public void setStreet(int street) {
		this.street = street;
	}
	public int getSubstreet() {
		return substreet;
	}
	public void setSubstreet(int substreet) {
		this.substreet = substreet;
	}
	public String getRepresentative() {
		return representative;
	}
	public void setRepresentative(String representative) {
		this.representative = representative;
	}
		
}
